package com.ir.form;

public class TrainingCenterReport {

	private String trainingPartner;
	private String createdDate ;
	private String trainingCenter;
	private String  noOfTrainingDone;
	private String noOfTraineesInvolved;
	private String pass;
	private String countFail;
	public String getTrainingPartner() {
		return trainingPartner;
	}
	public void setTrainingPartner(String trainingPartner) {
		this.trainingPartner = trainingPartner;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getTrainingCenter() {
		return trainingCenter;
	}
	public void setTrainingCenter(String trainingCenter) {
		this.trainingCenter = trainingCenter;
	}
	public String getNoOfTrainingDone() {
		return noOfTrainingDone;
	}
	public void setNoOfTrainingDone(String noOfTrainingDone) {
		this.noOfTrainingDone = noOfTrainingDone;
	}
	public String getNoOfTraineesInvolved() {
		return noOfTraineesInvolved;
	}
	public void setNoOfTraineesInvolved(String noOfTraineesInvolved) {
		this.noOfTraineesInvolved = noOfTraineesInvolved;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getCountFail() {
		return countFail;
	}
	public void setCountFail(String countFail) {
		this.countFail = countFail;
	}
	
	
}
